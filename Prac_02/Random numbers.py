import random
print(random.randint(5, 20))  # line 1

# line 1 prints a random number between 5 and 20. 5 being the smallest and 20 being the largest

print(random.randrange(3, 10, 2))  # line 2

# line 2 prints a random number between 3 and 10 but it can only be every second number.
# the smallest is a 3 and the largest a 9. Also it is not possible to get a 4

print(random.uniform(2.5, 5.5))  # line 3

# line 3 prints a random number between 2.5 and 5.5. 2.5000... being the smallest and 5.500... being the largest


